import Head from 'next/head'
import Header from '@/components/home/Header'
import InfoInputForm from '@/components/home/InfoInputForm'
import { ChangeEvent, useRef, useState } from 'react'
import Privacy from '@/components/home/Privacy';
import styles from "@/styles/Home.module.css";
import { useRouter } from 'next/router';
import { simpleInfo } from '@/types';

export default function Home() {
  let router = useRouter();

  const [info, setInfo] = useState<simpleInfo>({
    phone: '',
    ages: '',
    sex: '',
    check: false
  });

  const phoneRef = useRef<HTMLInputElement>(null);

  const onChangePhone = (e: ChangeEvent<HTMLInputElement>) => {
    if (phoneRef.current) {
      const value = phoneRef.current.value.replace(/\D+/g, "");
      const phoneLength = 11;
      let result = "";
      for (let i = 0; i < value.length && i < phoneLength; i++) {
        switch (i) {
          case 3:
            result += "-";
            break;
          case 7:
            result += "-";
            break;
          default:
            break;
        }
        result += value[i];
      }
      phoneRef.current.value = result;
      setInfo((prev) => ({
        ...prev,
        phone: e.target.value
      }));
    }
  }

  const onClickNext = () => {
    if (info.phone === "" || info.phone.length !== 13) {
      alert("핸드폰 번호를 입력해주세요.");
    } else if (info.sex === "") {
      alert("성별을 선택해주세요.");
    } else if (info.ages === "") {
      alert("연령대를 선택해주세요.");
    } else if (!info.check) {
      alert("개인정보 수집/이용에 동의해주세요.");
    } else {
      router.push('/survey')
    }

    console.log("info", info);
  }

  const onChangeCheck = () => {
    setInfo((prev) => ({
      ...prev,
      check: !info.check
    }))
  }

  const onChangeHandle = (e: ChangeEvent<HTMLInputElement>)=>{
    const {name, value} = e.target;
    setInfo(prev=>({
      ...prev,
      [name]: value
    }))
  }
  
  return (
    <>
      <Head>
        <title>Survey Temp</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <main className={styles.main}>
        <div className={styles.wrapper}>
          <InfoInputForm
            info={info}
            phoneRef={phoneRef}
            onChangePhone={onChangePhone}
            onChangeHandle={onChangeHandle}
          />
          <Privacy check={info.check} onChangeCheck={onChangeCheck} />
        </div>
        <div className={styles.btnCenter} onClick={onClickNext}>
          <button disabled={!info.check}>넘어가기</button>
        </div>
      </main>
    </>
  )
}
